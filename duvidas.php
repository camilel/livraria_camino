<div class="faq-container">
    <details>
  <summary>Quanto tempo leva para meu pedido ser entregue?</summary>
  <p>Os pedidos são processados em até 2 dias úteis e o prazo de entrega varia conforme a transportadora e sua região. Normalmente, o prazo total é de 5 a 10 dias úteis.</p>
</details>

<details>
  <summary>Vocês fazem entregas para todo o Brasil?</summary>
  <p>Sim, entregamos para todas as regiões do Brasil através de transportadoras parceiras e dos Correios.</p>
</details>

<details>
  <summary>Posso acompanhar o envio do meu pedido?</summary>
  <p>Sim. Assim que o pedido for enviado, você receberá um código de rastreamento por e-mail para acompanhar a entrega.</p>
</details>

<details>
  <summary>Vocês vendem livros digitais (e-books)?</summary>
  <p>Sim, oferecemos e-books em diversos formatos. Após a compra, o download é liberado instantaneamente na sua conta.</p>
</details>

<details>
  <summary>Posso trocar ou devolver um livro?</summary>
  <p>Sim, aceitamos devoluções e trocas em até 7 dias corridos após o recebimento, desde que o produto esteja em perfeito estado.</p>
</details>

<details>
  <summary>E se meu livro chegar danificado?</summary>
  <p>Entre em contato com nosso suporte enviando fotos do dano. Faremos a troca sem custo adicional.</p>
</details>

<details>
  <summary>Posso retirar meu pedido pessoalmente?</summary>
  <p>No momento, trabalhamos apenas com entregas, mas estamos avaliando oferecer retirada em loja física no futuro.</p>
</details>

<details>
  <summary>Quais formas de pagamento são aceitas?</summary>
  <p>Aceitamos cartões de crédito, débito, Pix e boleto bancário.</p>
</details>

<details>
  <summary>É possível reservar um lançamento antes do lançamento oficial?</summary>
  <p>Sim, temos a opção de pré-venda para lançamentos. Você garante seu exemplar e ele será enviado na data de lançamento.</p>
</details>

<details>
  <summary>Posso cancelar meu pedido após a compra?</summary>
  <p>Sim, desde que o pedido ainda não tenha sido enviado. Entre em contato com nosso suporte o quanto antes.</p>
</details>

